package chap03;

public class Char {
    public static void main(String[] args) {
        char a1 = 'a';          // 문자값
        char a2 = 97;           // 아스키코드값
        char a3 = '\u0061';     // 유니코드값

        System.out.println(a1); // a
        System.out.println(a2); // a
        System.out.println(a3); // a
    }
}
